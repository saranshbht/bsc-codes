    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(jLabel2.isVisible()){
            String s = jTextArea1.getText() + "Complete previous transaction first\n";
            jTextArea1.setText(s);
            return ; 
        }
        if(jLabel3.isVisible()){
            float balance = Float.parseFloat(jTextField2.getText());
            String s = jTextArea1.getText() + bnk.accsWithBal(balance) + "\n";
            jTextArea1.setText(s);
            jLabel3.setVisible(false);
            jTextField2.setVisible(false);
        }
        else{
            jLabel3.setVisible(true);
            jTextField2.setVisible(true);
        }
         
    }//GEN-LAST:event_jButton5ActionPerformed

