    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
         if(jLabel2.isVisible()){
             int accNo = Integer.parseInt(jTextField1.getText());
             float balance = Float.parseFloat(jTextField2.getText());
             bnk.addAccount(new BankAccount(accNo, balance));
             String s = jTextArea1.getText() + "Account added successfully\n";
             jTextArea1.setText(s);
             jLabel2.setVisible(false);
             jLabel3.setVisible(false);
             jTextField1.setVisible(false);
             jTextField2.setVisible(false);
         }
         else{
             jLabel2.setVisible(true);
             jLabel3.setVisible(true);
             jTextField1.setVisible(true);
             jTextField2.setVisible(true);
         }
        }
        catch(Exception e){
            String s = jTextArea1.getText() + e.getMessage() + "\n";
            jTextArea1.setText(s);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

